5. /proc #process information

6. /var #variable files

7. /tmp #temporary files

8. /usr #user sys resources

9. /home #home directories

10. /boot #boot loader files

11. /lib #system libraries

12. /opt #optional add on apps

13. /mnt #mount directory

14. /media #removable devices

15. /srv #service data
